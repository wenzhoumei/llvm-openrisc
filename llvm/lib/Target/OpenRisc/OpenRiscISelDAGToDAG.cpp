namespace {

class OpenRiscDAGToDAGISel : public SelectionDAGISel {
public:
  OpenRiscDAGToDAGISel(OpenRiscTargetMachine &TM, CodeGenOptLevel OptLevel)
      : SelectionDAGISel(TM, OptLevel) {}

  void Select(SDNode *Node) override;

  // For load/store instructions generate (base+offset) pair from
  // memory address.
  bool selectAddrRegImm(SDValue Addr, SDValue &Base, SDValue &Offset) {
    EVT ValTy = Addr.getValueType();

    // if Address is FI, get the TargetFrameIndex.
    if (FrameIndexSDNode *FIN = dyn_cast<FrameIndexSDNode>(Addr)) {
      Base = CurDAG->getTargetFrameIndex(FIN->getIndex(), ValTy);
      Offset = CurDAG->getTargetConstant(0, SDLoc(Addr), ValTy);

      return true;
    }

    if (TM.isPositionIndependent()) {
      DiagnosticInfoUnsupported Diag(CurDAG->getMachineFunction().getFunction(),
                                     "PIC relocations are not supported ",
                                     Addr.getDebugLoc());
      CurDAG->getContext()->diagnose(Diag);
    }

    if ((Addr.getOpcode() == ISD::TargetExternalSymbol ||
         Addr.getOpcode() == ISD::TargetGlobalAddress))
      return false;

    // Addresses of the form FI+const
    bool Valid = false;
    if (CurDAG->isBaseWithConstantOffset(Addr)) {
      ConstantSDNode *CN = dyn_cast<ConstantSDNode>(Addr.getOperand(1));

      if (Valid) {
        // If the first operand is a FI, get the TargetFI Node
        if (FrameIndexSDNode *FIN =
                dyn_cast<FrameIndexSDNode>(Addr.getOperand(0)))
          Base = CurDAG->getTargetFrameIndex(FIN->getIndex(), ValTy);
        else
          Base = Addr.getOperand(0);

        Offset =
            CurDAG->getTargetConstant(CN->getZExtValue(), SDLoc(Addr), ValTy);
        return true;
      }
    }

    // Last case
    Base = Addr;
    Offset = CurDAG->getTargetConstant(0, SDLoc(Addr), Addr.getValueType());
    return true;
  }

// Include the pieces autogenerated from the target description.
#include "OpenRiscGenDAGISel.inc"
}; // namespace

class OpenRiscDAGToDAGISelLegacy : public SelectionDAGISelLegacy {
public:
  static char ID;

  OpenRiscDAGToDAGISelLegacy(OpenRiscTargetMachine &TM, CodeGenOptLevel OptLevel)
      : SelectionDAGISelLegacy(
            ID, std::make_unique<OpenRiscDAGToDAGISel>(TM, OptLevel)) {}

  StringRef getPassName() const override {
    return "OpenRisc DAG->DAG Pattern Instruction Selection";
  }
};
} // end anonymous namespace

char OpenRiscDAGToDAGISelLegacy::ID = 0;

FunctionPass *llvm::createOpenRiscISelDag(OpenRiscTargetMachine &TM,
                                        CodeGenOptLevel OptLevel) {
  return new OpenRiscDAGToDAGISelLegacy(TM, OptLevel);
}

void OpenRiscDAGToDAGISel::Select(SDNode *Node) {
  SDLoc DL(Node);

  // If we have a custom node, we already have selected!
  if (Node->isMachineOpcode()) {
    Node->setNodeId(-1);
    return;
  }

  SelectCode(Node);
}
