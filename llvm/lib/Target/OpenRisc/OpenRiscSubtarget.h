#ifndef LLVM_LIB_TARGET_OPENRISC_OPENRISCSUBTARGET_H
#define LLVM_LIB_TARGET_OPENRISC_OPENRISCSUBTARGET_H

#include "OpenRiscFrameLowering.h"
#include "OpenRiscISelLowering.h"
#include "OpenRiscInstrInfo.h"
#include "OpenRiscRegisterInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/Target/TargetMachine.h"

#define GET_SUBTARGETINFO_HEADER
#include "OpenRiscGenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class OpenRiscSubtarget : public OpenRiscGenSubtargetInfo {
private:
  const Triple &TargetTriple;
  OpenRiscInstrInfo InstrInfo;
  OpenRiscTargetLowering TLInfo;
  SelectionDAGTargetInfo TSInfo;
  OpenRiscFrameLowering FrameLowering;

  OpenRiscSubtarget &initializeSubtargetDependencies(StringRef CPU, StringRef FS);

public:
  OpenRiscSubtarget(const Triple &TT, StringRef CPU, StringRef FS,
                  const TargetMachine &TM);

  const Triple &getTargetTriple() const { return TargetTriple; }

  const TargetFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const OpenRiscInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const OpenRiscRegisterInfo *getRegisterInfo() const override {
    return &InstrInfo.getRegisterInfo();
  }

  const OpenRiscTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }

  // Automatically generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);
};
} // end namespace llvm

#endif /* LLVM_LIB_TARGET_OPENRISC_OPENRISCSUBTARGET_H */
